package main.java.Models.ModelObject;

public class Cds {
    private String name;
    private String string;
    private String cd;

    public void setString(String string) {
        this.string = string;
    }

    public String getString() {
        return string;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setCd(String cd) {
        this.cd = cd;
    }

    public String getCd() {
        return cd;
    }
}

